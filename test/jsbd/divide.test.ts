import { JSBD } from '../../src/jsbd'

test('100 divide 2', () => {
  let a = JSBD.BigDecimal('100')
  let b = JSBD.BigDecimal('2')
  expect(
    JSBD.divide(a, b, {
      maximumFractionDigits: 2,
    }).toString()
  ).toBe('50')
})

test('0.2 divide 3', () => {
  let a = JSBD.BigDecimal('0.2')
  let b = JSBD.BigDecimal('3')
  expect(
    JSBD.divide(a, b, {
      maximumFractionDigits: 4,
      roundingMode: 'half even',
    }).toString()
  ).toBe('0.0667')
})

